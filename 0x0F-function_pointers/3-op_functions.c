#include "3-calc.h"
/**
 * op_add - calculate sum of integer
 * @a: first integer
 * @b: second integer
 * Return: sum of integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calculates sub of integer
 * @a: first integer
 * @b: second integer
 * Return: sub of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calculates the multiplication of two integers
 * @a: first integer
 * @b: second integer
 * Return: multi of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calculates the division of integer
 * @a: first integer
 * @b: second integer
 * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates the remainder of the division of integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of devision between and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
